public final class LogCustom {
    //是否打印Log
    private static   Boolean isEnable = true;

    public static Boolean getEnable() {
        return isEnable;
    }

    public static void setEnable(Boolean enable) {
        isEnable = enable;
    }

    public static void v(String msg) {
        if (isEnable){
            Log.v(Constants.TAG, msg)  ;
        }
    }


    public static void v(String msg, Throwable tr) {
        if (isEnable){
            Log.v(Constants.TAG, msg,tr)  ;
        }
    }


    public static void d( String msg) {
        if (isEnable){
            Log.d(Constants.TAG, msg)  ;
        }
    }


    public static void d( String msg, Throwable tr) {
        if (isEnable){
            Log.d(Constants.TAG, msg,tr)  ;
        }
    }


    public static void i( String msg) {
        if (isEnable){
            Log.i(Constants.TAG, msg)  ;
        }
    }

    public static void i( String msg, Throwable tr) {
        if (isEnable){
            Log.i(Constants.TAG, msg,tr)  ;
        }
    }


    public static void w( String msg) {
        if (isEnable){
            Log.w(Constants.TAG, msg)  ;
        }
    }


    public static void w( String msg, Throwable tr) {
        if (isEnable){
            Log.w(Constants.TAG, msg,tr)  ;
        }
    }

    public static void e( String msg) {
        if (isEnable){
            Log.e(Constants.TAG, msg)  ;
        }
    }


    public static void e( String msg, Throwable tr) {
        if (isEnable){
            Log.e(Constants.TAG, msg,tr)  ;
        }
    }
}
